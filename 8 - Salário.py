# Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o total do seu salário no referido mês.

a = int(input("Insira quanto você ganha por hora:"))
b = int(input("Insira quantas horas você trabalhou neste mês:"))

resultado = a*b

print("O seu salário neste mês é:", resultado)